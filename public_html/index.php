      <div class="animated fadeInDown" id="whoWeAre">
      <h3 style="font-family:'Droid Serif', serif;"><b>Who We Are</b></h3>
      <p>
      The Queen’s Hospitality and Tourism Club (QHTC) provides opportunities for Queen’s students to explore the thriving hospitality and tourism industry. We provide students with deeper insights into the industry at both the local Canadian level and international level.
      </p>
      <p>
      As a club we are committed to developing an integrated communication platform between Queens students and industry professionals through various valuable networking opportunities and exciting events. There are many ways for students to get involved with our club, such as, joining us for alumni and speaker series events. In addition, we’ll be providing students the opportunity to gain first-hand experiences with industry professionals through local visits to hotels, restaurants, and other popular tourism destinations within the Kingston vicinity and extending outwards throughout Ontario.
      </p>
      <p><b>
      QHTC welcomes students from diverse academic backgrounds and we strive to create a welcoming community amongst students. We’re always looking forward to meeting new faces from all faculties!
      </b></p>
