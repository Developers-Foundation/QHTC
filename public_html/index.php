      <h3 style="font-family:'Droid Serif', serif;"><b>Mandate</b></h3>
      <p>
      Queen’s Hospitality and Tourism Club’s mandate is to provide a common platform for students to openly explore the rapidly growing industry of hospitality and tourism. We strive to broaden members’ interest and knowledge in this relatively unexplored industry by providing unique opportunities for members to gain hands-on experiences. As a club, we also dedicated to building connections between students and real-life industry professionals, providing valuable networking experiences to help enhance members’ ability to pursue a future career in the industry. 
      </p>
      <p>
      Through the abundant exposure that members will gain in the industry we also aim to promote cultural diversity and communication among Queen’s students as well as Kingstonians. QHTC hopes to unite students from diverse academic backgrounds to create a welcoming community amongst students while engaging in exciting and fun events that will allow members to gain new knowledge and practical skills in the relatively unexplored industry of hospitality and tourism. 
      </p>
